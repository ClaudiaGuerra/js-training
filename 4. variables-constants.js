/*
  Hay 3 formas de declarar variables en JavaScript:
  1. var: Forma antigua, declara variables globales, es modificable
  2. let: Forma moderna, declara variables de bloque, es modificable
  3. const: Declaraci√≥n de constante, no pueden ser modificades
*/

var var1 = 'Hola viejo mundo'; // Variable global
let var2 = 'Hola nuevo mundo'; // Variable de bloque
const pi = 3.14159; // Constante
console.log('Var1:', var1, 'Var2:', var2, 'Pi:', pi);

var1 = 'Hola, viejo mundo MODIFICADO';
var2 = 10
pi = 2.73;
console.log('Var1:', var1, 'Var2:', var2, 'Pi:', pi);

