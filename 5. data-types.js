/*
  En JS tenemos 6 diferentes tipos de datos primitivos:
  - Number (enteros, decimales, negativos, etc.)
  - String (caracteres simples o cadenas de caracteres)
  - Boolean (true o false)
  - Null (ausencia intencional de valor)
  - Undefined (valor no asignado)
  - **Symbol (valor único e inmutable, usado principalmente como identificador de propiedades de objetos

  ¿Cuál es la diferencia entre null y undefined?
  - Null -> Un vaso de agua, sin agua (vacío) -> Se define de forma explícita
  - Undefined -> No hay un vaso de agua (no existe) -> Se define de forma implícita
*/

